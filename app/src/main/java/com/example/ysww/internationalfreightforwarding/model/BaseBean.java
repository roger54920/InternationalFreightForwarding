package com.example.ysww.internationalfreightforwarding.model;

/**
 * 基础类型
 */

public class BaseBean {

    /**
     * msg : success
     * code : 0
     */

    private String msg;
    private int code;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
